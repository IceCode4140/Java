
public class ch3_8 {
 public static void main(String[] args) {
  int x;
  long y;
  x = 103;
  System.out.printf(" print value of 103 = " + x);// decimal
  x = 0b111;
  System.out.printf(" print value of 0b111 = " + x);// binary
  y = 022;
  System.out.printf(" print value of 022 = " + y);// octal
  y = 0x2B;
  System.out.printf(" print value of 0x2B = " + y);// hexadecimal
 }

}